package com.example.thithu;

public class Hang {
    String ten, danhGia, gia, tile;
    int soSao;
    int hinh;

    public Hang(String ten, String danhGia, String gia, String tile, int soSao, int hinh) {
        this.ten = ten;
        this.danhGia = danhGia;
        this.gia = gia;
        this.tile = tile;
        this.soSao = soSao;
        this.hinh = hinh;
    }
}
